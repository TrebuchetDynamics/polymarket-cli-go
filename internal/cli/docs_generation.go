package cli

import (
	"fmt"
	"sort"
	"strings"

	"github.com/spf13/cobra"
	"github.com/spf13/pflag"
)

const commandDocsGenerator = "go run ./cmd/polygolem_docs"

type commandDocFlag struct {
	name        string
	shorthand   string
	valueType   string
	defaultText string
	usage       string
}

// GenerateCommandsMarkdown renders the checked-in CLI command reference from
// Cobra metadata. Keep this deterministic: generated docs are a build artifact.
func GenerateCommandsMarkdown(root *cobra.Command) string {
	prepareCommandDocs(root)

	var b strings.Builder
	b.WriteString("# Commands\n\n")
	b.WriteString("Generated command reference for `polygolem`.\n\n")
	b.WriteString("> Generated by `" + commandDocsGenerator + "`. Do not edit by hand.\n\n")
	b.WriteString("## Conventions\n\n")
	b.WriteString("- All commands accept `--json` to emit structured JSON.\n")
	b.WriteString("- Flag semantics come from the Cobra command tree in `internal/cli`.\n")
	b.WriteString("- Authenticated commands consume the environment variables listed below.\n")
	b.WriteString("- Trading commands use deposit-wallet signing only (type 3 / POLY_1271).\n\n")
	writeEnvironmentVariablesSection(&b)
	writeAutomationSmokeCheckSection(&b)
	b.WriteString("## Command Tree\n\n")
	b.WriteString("```text\n")
	writeCommandTree(&b, root, 0)
	b.WriteString("```\n\n")
	b.WriteString("## Command Reference\n\n")
	for _, cmd := range commandList(root) {
		writeCommandSection(&b, cmd)
	}
	return strings.TrimRight(b.String(), "\n") + "\n"
}

